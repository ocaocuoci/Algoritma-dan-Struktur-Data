public class MataKuliah{
    public String kode;
    public String nama;
    public int sks;
    public int jumlahSks;

    public MataKuliah(String kode, String nama, int sks, int jumlahSks){
        this.kode = kode;
        this.nama = nama;
        this.sks = sks;
        this.jumlahSks = jumlahSks;
    }
}